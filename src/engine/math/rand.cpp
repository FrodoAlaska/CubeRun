#include "rand.h"
#include "engine/defines.h"

#include <random>

// GLOBALS
/////////////////////////////////////////////////////////////////////////////////
static std::random_device rand_device;
static std::default_random_engine engine(rand_device());
/////////////////////////////////////////////////////////////////////////////////

// Public functions
/////////////////////////////////////////////////////////////////////////////////
u32 random_u32() {
  std::uniform_int_distribution<u32> uni_dist;
  return uni_dist(engine);
}
